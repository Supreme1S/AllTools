type RawService = Omit<Service, "domain" | "outbound_url" | "image"> &
  Partial<Pick<Service, "domain" | "outbound_url" | "image">>;

function normalizeService(raw: RawService): Service {
  let domain = raw.domain ?? "";
  if (!domain) {
    try {
      domain = new URL(raw.website_url).hostname.replace(/^www\./, "");
    } catch {
      domain = raw.slug;
    }
  }
  return {
    ...raw,
    domain,
    outbound_url: raw.outbound_url ?? raw.referral_url ?? raw.website_url,
    image: raw.image ?? null,
  };
}

